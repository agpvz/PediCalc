/** Look up a vital-sign value for a given year + month from a percentile table */
export function vL(tbl, yr, mo) {
  for (const [y, m, v] of tbl) {
    if (m > 0 ? yr === y && mo < m : yr < y) return v;
  }
  return null;
}

