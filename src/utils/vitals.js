/** Look up MAP percentile value by weight and sex (1=M, 2=F) */
export function mL(w, s, p) {
  const t = s === 1 ? MB : MG;
  const v = t[p];
  if (!v) return null;
  for (let i = 0; i < BPT.length; i++) if (w < BPT[i]) return v[i];
  return null;
}
